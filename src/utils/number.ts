/**
 * A utility function that multiplies a value by a unit.
 *
 * @param {number} value - The value to be multiplied.
 * @param {number} unit - The unit to multiply the value by.
 * @returns {number} The result of the multiplication.
 */
export const number = (value: number, unit: number) => {
  return value * unit;
};

